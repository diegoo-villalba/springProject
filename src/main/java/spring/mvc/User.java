package spring.mvc;

public class User {

	private String name;
	private String lastname;

	public void setName(String name) {
		this.name = name;
	}

	public void setLastname(String lastName) {
		this.lastname = lastName;
	}

	public String getName() {
		return name;
	}

	public String getLastname() {
		return lastname;
	}

}
